function add(a, b) {
    return a + b ;
}   
function subtract(a, b) {
    return a - b;
}
function multiply(a, b) {
    return a * b;
}
function divide(a, b) {
    return a / b
}
function increment(n){
    var m = n + 1
    return m
}
function decrement(n){
    var m = n - 1
    return m
}
function makeInt(n){
    return parseInt(n, 10)
}
function preserveDecimal(n){
    return parseFloat(n)
}